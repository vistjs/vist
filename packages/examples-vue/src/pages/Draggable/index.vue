<template>
  <div class="row">
    <div class="col-4">
      <h3>First draggable with footer</h3>

      <draggable id="first" data-source="juju" :list="list" class="list-group" group="a" item-key="name">
        <template #item="{ element }">
          <div class="list-group-item">
            {{ element.name }}
          </div>
        </template>

        <template #footer>
          <div class="btn-group list-group-item" role="group">
            <button class="btn btn-secondary" @click="add">Add</button>
            <button class="btn btn-secondary" @click="replace">Replace</button>
          </div>
        </template>
      </draggable>
    </div>

    <div class="col-4">
      <h3>Second draggable with header</h3>

      <draggable :list="list2" class="list-group" group="a" item-key="name">
        <template #item="{ element }">
          <div class="list-group-item item">
            {{ element.name }}
          </div>
        </template>

        <template #header>
          <div class="btn-group list-group-item" role="group" aria-label="Basic example">
            <button class="btn btn-secondary" @click="add2">Add</button>
            <button class="btn btn-secondary" @click="replace2">Replace</button>
          </div>
        </template>
      </draggable>
    </div>

    <raw-displayer class="col-2" :value="list" title="List" />

    <raw-displayer class="col-2" :value="list2" title="List2" />
  </div>
</template>

<script>
import draggable from 'vuedraggable';
import rawDisplayer from './RawDisplayer.vue';

let id = 1;
export default {
  name: 'two-list-headerslots',
  display: 'Two list header slot',
  order: 14,
  components: {
    draggable,
    rawDisplayer,
  },
  data() {
    return {
      list: [
        { name: 'John 1', id: 0 },
        { name: 'Joao 2', id: 1 },
        { name: 'Jean 3', id: 2 },
      ],
      list2: [
        { name: 'Jonny 4', id: 3 },
        { name: 'Guisepe 5', id: 4 },
      ],
    };
  },
  methods: {
    add: function () {
      this.list.push({ name: 'Juan ' + id, id: id++ });
    },
    replace: function () {
      this.list = [{ name: 'Edgard', id: id++ }];
    },
    add2: function () {
      this.list2.push({ name: 'Juan ' + id, id: id++ });
    },
    replace2: function () {
      this.list2 = [{ name: 'Edgard', id: id++ }];
    },
  },
};
</script>
